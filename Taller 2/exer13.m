%HECHO CON SELECTIVAS 
A= input('Ingrese el valor de A: ');
B= input('Ingrese el valor de B: ');

if A<B
    disp([num2str(A), ' es menor que ', num2str(B)])
    
elseif A>B
    disp([num2str(A), ' es mayor que ', num2str(B)])
    
elseif A==B
    disp([num2str(A), ' es igual que ', num2str(B)])
    
end




















