%Dados dos valores numericos A y B, escribir un mensaje diciendo si A es mayor, menor o
%igual a B.

A= input('el valor de A es: ');
B= input('el valor de B es: ');

if A>B
    disp('A es mayor que B');
elseif A<B
    disp('A es menor que B');
elseif A==B
    disp('A es igual que B');
end
    
   
